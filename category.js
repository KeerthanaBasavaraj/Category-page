$(document).ready(function () {
  $(".list").click(function () {
    var value = $(this).attr("data-filter");

    if (value == "all") {
      $(".itemBox").show("1000");
    } else {
      $(".itemBox")
        .not("." + value)
        .hide("3000");
      $(".itemBox")
        .filter("." + value)
        .show("3000");
    }
  });

  $(".list").click(function () {
    $(this).addClass("active").siblings().removeClass("active");
  });
});
